package whilee.loop;

public class WhileLoopClass {

	public static void main(String[] args) {

		int a = 6;

		while (a < 8) {
			System.out.println("Hi inside while loop");
			a++;
		}
		
	
		
		
		for (;a<8;)
		{
			System.out.println("Hi inside while loop");
			a++;
		}
		while (true)
		{
			System.out.println("inside while loop");
		}
		

	}

}
