package operators;

public class LogicalOperators {

	public static void main(String[] args) {
		//Logical operators    && , ||
		int a =5;
		int b =4;
		
		boolean f1 = true;
		boolean f2 = false;
		
		boolean f5 = (a<b) && f2;
		System.out.println(f5);
		
		boolean f4 = f1||f2;
		System.out.println(f4);
		boolean f3 = f1&&f2;
		System.out.println(f3);
		
		
		
		

	}

}
