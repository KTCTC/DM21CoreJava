package operators;

public class RelationalOperator {

	public static void main(String[] args) {
		//Relational operator <,>,<=,>=,==,!=
		
		int a = 560;
		int b = 56;
		
		boolean f6 = a!=b;
		System.out.println(f6);
		boolean f5 = a==b;
		System.out.println(f5);		
		boolean f4 = a>=b;
		System.out.println(f4);
		boolean f3 = a<=b;
		System.out.println(f3);
		boolean f2 = a>b;
		System.out.println(f2);
		boolean f1 = a<b;
		System.out.println(f1);
		
		   

	}

}
