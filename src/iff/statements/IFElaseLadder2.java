package iff.statements;

public class IFElaseLadder2 {

	public static void main(String[] args) {

		char ch = 'a';

		if (ch == 'A' || ch=='a') {
			System.out.println("Given character is vowel and = " + ch);

		} else if (ch == 'E'|| ch=='e') {
			System.out.println("Given character is vowel and = " + ch);
		} else if (ch == 'I'|| ch=='i') {
			System.out.println("Given character is vowel and = " + ch);
		} else if (ch == 'O'|| ch=='o') {
			System.out.println("Given character is vowel and = " + ch);
		} else if (ch == 'U'|| ch=='u') {
			System.out.println("Given character is vowel and = " + ch);
		} else {
			System.out.println("Given character is not vowel");
		}

	}

}
